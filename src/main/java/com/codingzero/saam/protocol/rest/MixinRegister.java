        mapper.addMixIn(OAuthIdentifierPolicyUpdateRequest.class, OAuthIdentifierPolicyUpdateRequestModel.class);
        mapper.addMixIn(UserRegisterWithOAuthRequest.class, UserRegisterWithOAuthRequestModel.class);
        mapper.addMixIn(UserRegisterWithOAuthRequest.OAuthIdentifier.class, UserRegisterRequestOAuthIdentifierModel.class);
        mapper.addMixIn(UserRegisterWithIdentifierRequest.class, UserRegisterWithIdentifierRequestModel.class);
        mapper.addMixIn(UserRegisterRequest.class, UserRegisterRequestModel.class);
